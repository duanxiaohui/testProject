$(function(){
	var instructions =$(".instructions");
	instructions.on('tap',function(){
		$(".instructions_layer").show();
		var w=$(".instructions_layer").height();
		$(".instructions_layer_div").height(w-12);
	});
	$(".colse_btn").on('tap',function(){
		$(".instructions_layer").hide();
	});
})